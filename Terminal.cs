        public static void Init()
        {
            Sys.Graphics.VGAScreen.SetTextMode(console_size);
            Console.SetWindowSize(GetWidth(), GetHeight());

            if(GetFontHeight() == 16)
            {
                Sys.Graphics.VGAScreen.SetFont(FontArray16, 16);
            }
            else
            {
                Sys.Graphics.VGAScreen.SetFont(FontArray8, 8);
            }

            Encoding.RegisterProvider(Sys.ExtendedASCII.CosmosEncodingProvider.Instance);
            Console.InputEncoding = Encoding.GetEncoding(866);
            Console.OutputEncoding = Encoding.GetEncoding(866);

            Console.Clear();
        }

        public static int GetWidth()
        {
